export interface AaveReserveData {
  currentLiquidityRate: bigint;
  aTokenAddress: string;
}

export function rayToPercent(ray: bigint): number {
  const RAY = 10n ** 27n;
  const percentBigInt = (ray * 10000n) / RAY;
  return Number(percentBigInt) / 100;
}

export function formatApy(liquidityRate: bigint): string {
  const apy = rayToPercent(liquidityRate);
  return `${apy.toFixed(2)}%`;
}

